package JavaCool303;

import java.awt.Color;

/**
 * <h2> Cool303Theme_Pastel </h2>
 * @author LeilaErbay
 * <p> Special Note: Extends Cool303Theme</p>
 * <p> Side effects: Instantiates the pastel-like colors</p>
 */
public class Cool303Theme_Pastel extends Cool303Theme {

	{
		buttonColor = (Color.magenta).brighter();
		boxColor = (Color.orange).brighter();
		rootColor = (Color.cyan).brighter();
		theme = "pastel";
	}
	
	
}
